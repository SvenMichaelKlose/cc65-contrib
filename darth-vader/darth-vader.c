int main(int argc, char* argv[])
{
  unsigned i=0;
  unsigned char j, n;
  unsigned char x0, y0, x, y;

  if (argc==1)
    tgi_install(tgi_static_stddrv);
  else
    tgi_load_driver(argv[1]);

  if (tgi_geterror()!=TGI_ERR_OK)
    return 1;

  tgi_init();
  tgi_clear();

  for (;;)
  {
    n=data[i++];
    //printf("%d\n", n);
    if (n==1)
      break;
    x0=data[i++];
    y0=data[i++];
    for (j=1; j<n; ++j)
    {
      x=data[i++];
      y=data[i++];
      //printf("%d %d,%d\n", j, x, y);
      tgi_line(x0, tgi_getyres()-y0+20, x, tgi_getyres()-y+20);
      x0=x;
      y0=y;
    }
  }

  getchar();
  tgi_done();
}
